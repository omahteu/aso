// Função para adicionar opções ao select
export function populaSelectEstados(selectElement) {
    for (const [sigla, nome] of Object.entries(estadosBrasileiros)) {
        const option = document.createElement("option");
        option.value = sigla;
        option.text = nome;
        selectElement.appendChild(option);
    }
}
